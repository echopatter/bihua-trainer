// scripts/validate-characters.mjs
// Build-time validation of curated characters: compare cnchar's stroke names
// against the 28-stroke table; exclude and report any mismatch (spec, game
// mode 4). Also emits pinyin for every character shown in the UI. Writes:
//   data/generated/char-validation.js  (validated chars + precomputed names)
//   data/generated/char-validation.md  (report, checkpoint 6)
//   data/generated/char-pinyin.js      (char -> pinyin, from cnchar)

import { createRequire } from 'node:module';
import { writeFileSync } from 'node:fs';
import { CHARACTERS } from '../data/characters.js';
import { STROKES } from '../data/strokes.js';

const require = createRequire(import.meta.url);
const cnchar = require('cnchar');
cnchar.use(require('cnchar-order'));

const KNOWN = new Set(STROKES.map(s => s.name));

// Known cnchar naming-scheme differences, documented during glyph extraction
// (see data/generated/extraction-report.md). table name -> cnchar alias(es).
const VARIANT_EQUIV = {
  '竖折': ['竖弯'],           // cnchar calls 山#1 竖弯
  '横折弯钩': ['横斜钩'],      // cnchar calls 九#1/乙 横斜钩
  '弯钩': ['竖钩'],           // Kai vs Song: cnchar calls 了#1 竖钩
};

const stripDigits = s => s.replace(/[0-9]/g, ''); // cnchar returns e.g. 点2
const variantsAt = (names, i) => String(names[i]).split('|').map(stripDigits);
const nameMatches = (want, variants) =>
  variants.includes(want) || (VARIANT_EQUIV[want] ?? []).some(v => variants.includes(v));

const validated = [];
const excluded = [];
const rows = [];

for (const entry of CHARACTERS) {
  const raw = cnchar.stroke(entry.char, 'order', 'name');
  const names = Array.isArray(raw[0]) ? raw[0] : raw;
  const problems = [];
  const warnings = [];

  if (!names.length || names.some(n => !n)) {
    problems.push('cnchar returned no stroke names');
  }
  if (entry.expected) {
    if (entry.expected.length !== names.length) {
      problems.push(`expected ${entry.expected.length} strokes, cnchar has ${names.length}`);
    } else {
      entry.expected.forEach((want, i) => {
        if (!nameMatches(want, variantsAt(names, i))) {
          problems.push(`stroke ${i + 1}: expected ${want}, cnchar says ${names[i]}`);
        }
      });
    }
  }
  for (const want of entry.includes ?? []) {
    const hit = names.some((_, i) => nameMatches(want, variantsAt(names, i)));
    if (!hit) problems.push(`must include ${want} (table row), cnchar has [${names.join(', ')}]`);
  }
  names.forEach((n, i) => {
    const vs = variantsAt(names, i);
    if (!vs.some(v => KNOWN.has(v))) {
      warnings.push(`stroke ${i + 1} '${n}' is not in the 28+extended stroke set`);
    }
  });

  if (problems.length) {
    excluded.push({ char: entry.char, problems });
    rows.push(`| ${entry.char} | EXCLUDED | ${problems.join('; ')} |`);
  } else {
    validated.push({
      char: entry.char,
      hsk: entry.hsk ?? null,
      names: names.map((_, i) => variantsAt(names, i)),
    });
    rows.push(`| ${entry.char} | ok${warnings.length ? ' (warn)' : ''} | ${warnings.join('; ') || '-'} |`);
  }
}

writeFileSync(new URL('../data/generated/char-validation.js', import.meta.url),
  `// data/generated/char-validation.js
// AUTO-GENERATED by scripts/validate-characters.mjs. Do not edit.
// Curated characters whose cnchar stroke names passed validation against the
// 28-stroke table; names are precomputed (array of |variant arrays per
// stroke) so the app needs no cnchar at runtime for curated characters.
// Known naming-scheme equivalences applied: ${JSON.stringify(VARIANT_EQUIV)}
export const VALIDATED_CHARS = ${JSON.stringify(validated, null, 1)};
export const EXCLUDED_CHARS = ${JSON.stringify(excluded, null, 1)};
export const VARIANT_EQUIV = ${JSON.stringify(VARIANT_EQUIV, null, 1)};
`);

const pad = n => String(n).padStart(2, '0');
const now = new Date();
const stamp = `${pad(now.getDate())}/${pad(now.getMonth() + 1)}/${now.getFullYear()} ${pad(now.getHours())}:${pad(now.getMinutes())}`;
writeFileSync(new URL('../data/generated/char-validation.md', import.meta.url), [
  '# Curated character validation report', '',
  `Generated ${stamp} by scripts/validate-characters.mjs`,
  '',
  `${validated.length} validated, ${excluded.length} excluded.`,
  '',
  '| char | status | notes |',
  '|------|--------|-------|',
  ...rows, '',
  'Naming-scheme equivalences applied (documented in extraction-report.md):',
  ...Object.entries(VARIANT_EQUIV).map(([k, v]) => `- table ${k} ≈ cnchar ${v.join('/')}`),
].join('\n') + '\n');

// Pinyin for every character the UI displays: curated characters plus all
// example characters from the stroke table. From cnchar; no hand-written
// pinyin.
const shown = new Set(CHARACTERS.map(c => c.char));
for (const s of STROKES) {
  for (const c of [...(s.examples ?? []), ...(s.rareExamples ?? []), s.onlyExample]) {
    if (c) shown.add(c);
  }
}
const pinyin = {};
for (const c of [...shown].sort()) {
  // 'poly' lists every reading, e.g. 长 -> (cháng|zhǎng); show all rather
  // than risk a misleading single reading
  const p = cnchar.spell(c, 'low', 'tone', 'poly');
  if (p && p !== c) pinyin[c] = p.replace(/[()]/g, '').split('|').join('/');
}
writeFileSync(new URL('../data/generated/char-pinyin.js', import.meta.url),
  `// data/generated/char-pinyin.js
// AUTO-GENERATED by scripts/validate-characters.mjs. Do not edit.
// All readings per character, from cnchar (used for UI pinyin brackets).
export const CHAR_PINYIN = ${JSON.stringify(pinyin, null, 1)};
`);

console.log(`${validated.length} validated, ${excluded.length} excluded, ${Object.keys(pinyin).length} pinyin entries.`);
for (const e of excluded) console.log(`  EXCLUDED ${e.char}: ${e.problems.join('; ')}`);
